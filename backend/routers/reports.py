@router.get("/")
async def list_reports(current_user: dict = Depends(get_current_user)):
    """Return all reports generated by the current user."""
    reports = await report_service.get_user_reports(current_user["uid"])
    return {"reports": reports, "total": len(reports)}


@router.get("/{report_id}")
async def get_report(report_id: str, current_user: dict = Depends(get_current_user)):
    """Return a single report record by ID."""
    doc = await _report_svc.get(report_id)
    if not doc:
        raise HTTPException(status_code=404, detail="Report not found.")
    if doc.get("user_id") != current_user["uid"] and current_user.get("role") != "admin":
        raise HTTPException(status_code=403, detail="Access denied.")
    return doc
